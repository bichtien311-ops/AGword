# -*- coding: utf-8 -*-
"""Автоматическая сборка учебного пособия «Синхронные машины» из множества файлов."""
import sys, os, re, glob
sys.path.insert(0, os.path.join(os.path.dirname(__file__), "execution"))

from word_builder import create_document, DocumentMetadata, DocType
from datetime import datetime

ROOT = os.path.dirname(__file__)
CONTENT_DIR = os.path.join(ROOT, "Синхронные_машины")
OUT = os.path.join(ROOT, "Результаты", "методичка_СМ.docx")

if not os.path.exists(os.path.join(ROOT, "Результаты")):
    os.makedirs(os.path.join(ROOT, "Результаты"))

# 1. Сбор всех Markdown файлов
# Ищем все файлы .md в папке Синхронные_машины и подпапках
md_files = []
for root_dir, dirs, files in os.walk(CONTENT_DIR):
    for f in files:
        if f.endswith('.md'):
            md_files.append(os.path.join(root_dir, f))

# Сортируем файлы. Благодаря правильным префиксам (00_, 01_, Л01_, Л02_...)
# они выстроятся в правильном хронологическом порядке.
md_files.sort()

# Исключаем Литературу, так как она пойдет в параметр sources
text_files = [f for f in md_files if "01_Литература" not in f]

full = ""
for file_path in text_files:
    with open(file_path, encoding="utf-8") as f:
        # Добавляем разрыв страницы (опционально) или просто два переноса
        full += f"\n\n" + f.read() + "\n\n"

# Сдвигаем заголовки на уровень выше, если нужно
# full = re.sub(r"^##(#{0,4})( )", r"#\1\2", full, flags=re.MULTILINE)

# 2. Добавление ссылок на сгенерированные графики (интеграция)
# Поскольку в исходных лекциях ссылок на рисунки может не быть, 
# мы можем программно вставить их в конец соответствующих разделов или лекций, 
# но проще оставить их генерацию как есть (скрипт Word сам подтянет, если есть ссылки).
# Если их нет, мы просто добавим раздел "Приложение с графиками"
appendix = """
# Приложение. Характеристики синхронных машин

![Рисунок П.1 - U-образные характеристики синхронного двигателя](Синхронные_машины/figures/fig_u_curves.png)

![Рисунок П.2 - Угловые характеристики мощности P(θ)](Синхронные_машины/figures/fig_angular_char.png)

![Рисунок П.3 - Внешние характеристики синхронного генератора](Синхронные_машины/figures/fig_external_char.png)
"""
full += appendix

# 3. Метаданные
introduction = (
    "Настоящее учебное пособие представляет собой фундаментальный курс по теории, "
    "конструкции и режимам работы синхронных машин (СМ). Синхронные генераторы "
    "являются основным источником электрической энергии в мире, вырабатывая более "
    "95% всей электроэнергии на тепловых, атомных и гидравлических станциях. "
    "Синхронные двигатели широко применяются в сверхмощных приводах (компрессоры, "
    "насосы, мельницы), а также выполняют важнейшую функцию компенсаторов "
    "реактивной мощности в сетях. Материал разбит на 12 лекций, охватывающих "
    "все аспекты работы: от уравнения ЭДС и реакции якоря до сложных режимов "
    "параллельной работы с сетью и анализа угловых характеристик статической "
    "устойчивости. Пособие будет незаменимо для подготовки специалистов в "
    "области электроэнергетики и электротехники."
)

conclusion = (
    "Освоение изложенного в пособии материала позволяет сформировать целостное "
    "представление о физической природе электромагнитных и электромеханических "
    "процессов в синхронных машинах. Подробно разобраны принципы возникновения "
    "реакции якоря (продольно-размагничивающей и поперечной), выведены векторные "
    "диаграммы неявнополюсных и явнополюсных машин. Изучение U-образных и "
    "угловых характеристик дает ключ к пониманию механизмов регулирования "
    "активной и реактивной мощности в энергосистемах. Знание условий включения "
    "генераторов на параллельную работу (метод точной синхронизации и самосинхронизации) "
    "критически важно для диспетчерского управления электростанциями. Полученные "
    "знания являются фундаментом для последующего изучения переходных процессов "
    "в электроэнергетических системах и релейной защиты."
)

sources = [
    "Иванов-Смоленский А.В. Электрические машины: учебник для вузов: в 2 т. — М.: Издательский дом МЭИ, 2006.",
    "Вольдек А.И., Попов В.В. Электрические машины. Машины переменного тока. — СПб.: Питер, 2010.",
    "Копылов И.П. Электрические машины: учебник для вузов. — М.: Высшая школа, 2006.",
    "Кацман М.М. Электрические машины: учебник. — М.: Академия, 2011.",
    "ГОСТ 533-2000. Генераторы синхронные турборежимные. Общие технические условия.",
    "ГОСТ 5616-89. Генераторы синхронные гидротурбинные. Общие технические условия."
]

meta = DocumentMetadata(
    title="Синхронные машины",
    university="",
    author="",
    discipline="",
    city="",
    year=str(datetime.now().year),
)

print(f"Сборка документа из {len(text_files)} файлов...")
create_document(
    doc_type=DocType.STUDY_GUIDE,
    raw_content=full,
    metadata=meta,
    output_path=OUT,
    sources=sources,
    introduction=introduction,
    conclusion=conclusion,
)
print("Успешно собрано в:", OUT)
