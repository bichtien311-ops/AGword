# -*- coding: utf-8 -*-
"""Финальная сборка учебного пособия «Асинхронные машины»."""
import sys, os, re
sys.path.insert(0, os.path.join(os.path.dirname(__file__), "execution"))

from word_builder import create_document, DocumentMetadata, DocType
from datetime import datetime

ROOT = os.path.dirname(__file__)
MASTER = os.path.join(ROOT, "data", "master_am.md")
OUT = os.path.join(ROOT, "Результаты", "методичка_АМ.docx")

with open(MASTER, encoding="utf-8") as f:
    full = f.read()

# Сдвигаем заголовки на уровень выше
full = re.sub(r"^##(#{0,4})( )", r"#\1\2", full, flags=re.MULTILINE)

introduction = (
    "Учебное пособие посвящено теории, конструкции и особенностям эксплуатации "
    "асинхронных машин (АМ) — наиболее распространенного типа электромеханических "
    "преобразователей энергии в современной промышленности. Простота конструкции, "
    "высокая надежность и низкая стоимость сделали асинхронные двигатели с "
    "короткозамкнутым ротором основой современного нерегулируемого и регулируемого "
    "электропривода. В данном курсе системно изложены физические принципы образования "
    "вращающегося магнитного поля, выведены основные уравнения напряжений и токов, "
    "построена Т-образная схема замещения. Подробно анализируется энергетическая "
    "диаграмма, дается строгий вывод формулы Клосса для электромагнитного момента "
    "и детально исследуются механические характеристики. Отдельное внимание "
    "уделяется современным способам пуска и частотного регулирования (по закону "
    "Костенко). Пособие содержит исчерпывающий математический аппарат и "
    "иллюстративный материал для глубокого понимания режимов работы машины."
)

conclusion = (
    "В пособии последовательно рассмотрены все ключевые аспекты теории асинхронных машин. "
    "Показано, что в основе принципа действия лежит взаимодействие вращающегося магнитного "
    "поля статора с индуцированными токами ротора, а наличие скольжения (s > 0) является "
    "необходимым условием создания электромагнитного момента. Доказана эквивалентность "
    "математических моделей асинхронного двигателя и трансформатора через приведение "
    "параметров вращающегося ротора к неподвижному статору. Выведенная формула Клосса "
    "позволила проанализировать зависимость вращающего момента от скольжения и обосновать "
    "пределы статической устойчивости двигателя (критический момент и критическое скольжение). "
    "Рассмотренные методы искусственного изменения характеристик, в первую очередь "
    "реостатное и частотное регулирование, открывают широкие возможности для применения "
    "АД в системах автоматизированного электропривода. Понимание энергетических процессов "
    "(потерь в меди и стали, зависимости КПД и cos φ от нагрузки) формирует базу для "
    "энергоэффективной эксплуатации электротехнических комплексов."
)

sources = [
    "Иванов-Смоленский А.В. Электрические машины: учебник для вузов: в 2 т. — М.: Издательский дом МЭИ, 2006.",
    "Вольдек А.И., Попов В.В. Электрические машины. Машины переменного тока. — СПб.: Питер, 2010.",
    "Копылов И.П. Электрические машины: учебник для вузов. — М.: Высшая школа, 2006.",
    "Кацман М.М. Электрические машины: учебник. — М.: Академия, 2011.",
    "ГОСТ Р 51689-2000. Двигатели асинхронные мощностью от 0,12 до 400 кВт включительно. Общие технические требования.",
    "Правила устройства электроустановок (ПУЭ). 7-е изд. — М.: Энергоатомиздат, 2003."
]

meta = DocumentMetadata(
    title="Асинхронные машины",
    university="",
    author="",
    discipline="",
    city="",
    year=str(datetime.now().year),
)

create_document(
    doc_type=DocType.STUDY_GUIDE,
    raw_content=full,
    metadata=meta,
    output_path=OUT,
    sources=sources,
    introduction=introduction,
    conclusion=conclusion,
)
print("OK:", OUT)
