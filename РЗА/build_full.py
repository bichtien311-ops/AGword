# -*- coding: utf-8 -*-
"""Финальная сборка учебного пособия «Релейная защита и автоматика»."""
import sys, os
sys.path.insert(0, os.path.join(os.path.dirname(__file__), "execution"))

from word_builder import create_document, DocumentMetadata, DocType
from datetime import datetime

ROOT = os.path.dirname(__file__)
MASTER = os.path.join(ROOT, "data", "master_rza.md")
OUT = os.path.join(ROOT, "Результаты", "методичка_РЗА.docx")

if not os.path.exists(os.path.join(ROOT, "Результаты")):
    os.makedirs(os.path.join(ROOT, "Результаты"))

with open(MASTER, encoding="utf-8") as f:
    full = f.read()

introduction = (
    "Учебное пособие «Релейная защита и автоматика» (РЗА) охватывает "
    "как классические электромеханические принципы обеспечения надежности "
    "электроэнергетических систем, так и современные подходы к построению "
    "цифровых подстанций на базе стандарта МЭК 61850. Релейная защита "
    "выполняет роль интеллектуального ядра энергосистемы, способного за "
    "доли секунды выявлять и локализовать аварийные режимы. В данном "
    "пособии подробно разбираются алгоритмы работы максимальных токовых, "
    "дифференциальных и дистанционных защит, методы согласования выдержек "
    "времени и расчета уставок, а также специфика обмена данными посредством "
    "протоколов GOOSE и Sampled Values."
)

conclusion = (
    "Изучение релейной защиты позволяет специалистам уверенно "
    "проектировать и эксплуатировать системы любой сложности. "
    "Переход отрасли к концепции цифровой подстанции означает не просто "
    "замену медных контрольных кабелей на оптоволокно, а кардинальное "
    "изменение принципов информационного взаимодействия оборудования. "
    "Понимание физических основ токовых и дистанционных защит, изложенных "
    "в первых модулях, является прочным фундаментом для последующей "
    "работы с современными микропроцессорными терминалами и логическими "
    "протоколами передачи данных."
)

sources = [
    "Шабад М.А. Расчеты релейной защиты и автоматики распределительных сетей. — СПб.: ПЭИПК, 2003.",
    "Чернобровов Н.В., Семенов В.А. Релейная защита энергетических систем. — М.: Энергоатомиздат, 1998.",
    "IEC 61850 Standard: Communication networks and systems in substations. Parts 1-10.",
    "Шнеерсон Э.М. Цифровая релейная защита. — М.: Энергоатомиздат, 2007.",
    "Дьяков А.Ф., Овчаренко Н.И. Микропроцессорная релейная защита и автоматика электроэнергетических систем. — М.: Издательство МЭИ, 2000."
]

meta = DocumentMetadata(
    title="Релейная защита и автоматика (РЗА)",
    university="",
    author="",
    discipline="",
    city="",
    year=str(datetime.now().year),
)

create_document(
    doc_type=DocType.STUDY_GUIDE,
    raw_content=full,
    metadata=meta,
    output_path=OUT,
    sources=sources,
    introduction=introduction,
    conclusion=conclusion,
)
print("OK:", OUT)
