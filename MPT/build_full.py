# -*- coding: utf-8 -*-
"""Финальная сборка учебного пособия «Машины постоянного тока»."""
import sys, os, re
sys.path.insert(0, os.path.join(os.path.dirname(__file__), "execution"))

from word_builder import create_document, DocumentMetadata, DocType
from datetime import datetime

ROOT = os.path.dirname(__file__)
MASTER = os.path.join(ROOT, "data", "master_mpt.md")
OUT = os.path.join(ROOT, "Результаты", "методичка_МПТ.docx")

with open(MASTER, encoding="utf-8") as f:
    full = f.read()

# Сдвигаем заголовки на уровень выше
full = re.sub(r"^##(#{0,4})( )", r"#\1\2", full, flags=re.MULTILINE)

introduction = (
    "Учебное пособие посвящено теории, конструкции и эксплуатации машин постоянного тока "
    "(МПТ) — фундаментальных электрических машин, играющих важнейшую роль в современных "
    "системах регулируемого электропривода. Уникальные свойства двигателей постоянного "
    "тока, такие как возможность плавного и широкого регулирования частоты вращения, высокая "
    "перегрузочная способность и огромный пусковой момент, делают их незаменимыми на "
    "транспорте, в металлургии и тяжелом машиностроении. Цель пособия — сформировать у "
    "обучающихся глубокое понимание электромагнитных процессов, протекающих в МПТ, "
    "научить анализировать механические и тормозные характеристики, а также решать "
    "практические инженерные задачи по расчету пусковых и регулировочных режимов. "
    "Материал структурирован по трем модулям и восьми лекциям: от физических основ, "
    "реакции якоря и сложнейших процессов коммутации до характеристик генераторов и "
    "управления двигателями. Каждая тема сопровождается аналитическим выводом уравнений, "
    "построением механических характеристик во всех квадрантах и наглядными иллюстрациями."
)

conclusion = (
    "В пособии последовательно изложены теоретические основы и практические аспекты работы "
    "машин постоянного тока. Доказано свойство обратимости МПТ, позволяющее им работать "
    "как в двигательном, так и в генераторном режимах. Детально изучено устройство "
    "якоря, коллектора и статора, выведены фундаментальные уравнения ЭДС (E = c_e Φ n) "
    "и электромагнитного момента (M = c_m Φ I_a). Особое внимание уделено анализу "
    "поперечной реакции якоря и причинам возникновения искрения при коммутации. "
    "Обосновано применение добавочных полюсов и компенсационных обмоток для обеспечения "
    "надежной работы машины. Рассмотрены внешние характеристики генераторов всех типов "
    "возбуждения. Для двигателей постоянного тока выведено универсальное уравнение "
    "механической характеристики, проанализированы свойства жестких и мягких характеристик, "
    "а также изучены методы пуска и три базовых способа регулирования частоты вращения: "
    "ослабление магнитного потока, изменение напряжения на якоре и введение "
    "добавочного сопротивления. Разбор рекуперативного, динамического и торможения "
    "противовключением формирует надежную базу для проектирования высокоэффективных "
    "автоматизированных электроприводов постоянного тока."
)

sources = [
    "Иванов-Смоленский А.В. Электрические машины: учебник для вузов: в 2 т. — М.: Издательский дом МЭИ, 2006.",
    "Вольдек А.И., Попов В.В. Электрические машины. Машины постоянного тока. — СПб.: Питер, 2010.",
    "Копылов И.П. Электрические машины: учебник для вузов. — М.: Высшая школа, 2006.",
    "Кацман М.М. Электрические машины: учебник. — М.: Академия, 2011.",
    "ГОСТ 183-74. Машины электрические вращающиеся. Общие технические условия.",
    "Правила устройства электроустановок (ПУЭ). 7-е изд. — М.: Энергоатомиздат, 2003."
]

meta = DocumentMetadata(
    title="Машины постоянного тока",
    university="",
    author="",
    discipline="",
    city="",
    year=str(datetime.now().year),
)

create_document(
    doc_type=DocType.STUDY_GUIDE,
    raw_content=full,
    metadata=meta,
    output_path=OUT,
    sources=sources,
    introduction=introduction,
    conclusion=conclusion,
)
print("OK:", OUT)
