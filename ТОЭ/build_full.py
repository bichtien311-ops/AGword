# -*- coding: utf-8 -*-
"""Финальная сборка учебного пособия «Теоретические основы электротехники»."""
import sys, os
sys.path.insert(0, os.path.join(os.path.dirname(__file__), "execution"))

from word_builder import create_document, DocumentMetadata, DocType
from datetime import datetime

ROOT = os.path.dirname(__file__)
MASTER = os.path.join(ROOT, "data", "master_toe.md")
OUT = os.path.join(ROOT, "Результаты", "методичка_ТОЭ.docx")

if not os.path.exists(os.path.join(ROOT, "Результаты")):
    os.makedirs(os.path.join(ROOT, "Результаты"))

with open(MASTER, encoding="utf-8") as f:
    full = f.read()

introduction = (
    "Учебное пособие «Теоретические основы электротехники» (ТОЭ) "
    "представляет собой фундаментальный курс, являющийся базисом "
    "для изучения всех электротехнических и радиотехнических дисциплин. "
    "В пособии излагаются основные законы, методы расчета линейных "
    "и нелинейных электрических цепей постоянного, однофазного "
    "синусоидального и трехфазного токов. Особое внимание уделено "
    "теории электромагнитного поля, базирующейся на уравнениях Максвелла, "
    "а также детальному анализу переходных процессов в электрических цепях, "
    "расчет которых выполняется как классическим, так и операторным методами. "
    "Пособие проиллюстрировано множеством наглядных графиков и векторных диаграмм."
)

conclusion = (
    "Изучение теоретических основ электротехники завершается формированием "
    "у студентов целостного физического понимания электромагнитных процессов. "
    "Освоенный математический аппарат — от комплексного метода для цепей "
    "переменного тока до преобразования Лапласа для расчета переходных "
    "режимов — позволит успешно моделировать и анализировать любые "
    "киберфизические электроэнергетические системы. Теория электромагнитного "
    "поля, изложенная в завершающем разделе, открывает путь к пониманию "
    "работы высокочастотных устройств, антенн и волноводов, составляющих "
    "основу современных систем связи и телемеханики."
)

sources = [
    "Бессонов Л.А. Теоретические основы электротехники. Электрические цепи. — М.: Высшая школа, 1996.",
    "Нейман Л.Р., Демирчян К.С. Теоретические основы электротехники: В 2-х т. — Л.: Энергоиздат, 1981.",
    "Атабеков Г.И. Теоретические основы электротехники. Линейные электрические цепи. — М.: Энергия, 1978.",
    "Демирчян К.С., Нейман Л.Р., Коровкин Н.В., Чечурин В.Л. Теоретические основы электротехники. — СПб.: Питер, 2003.",
    "Зевеке Г.В., Ионкин П.А., Нетушил А.В., Страхов С.В. Основы теории цепей. — М.: Энергия, 1975."
]

meta = DocumentMetadata(
    title="Теоретические основы электротехники",
    university="",
    author="",
    discipline="",
    city="",
    year=str(datetime.now().year),
)

create_document(
    doc_type=DocType.STUDY_GUIDE,
    raw_content=full,
    metadata=meta,
    output_path=OUT,
    sources=sources,
    introduction=introduction,
    conclusion=conclusion,
)
print("OK:", OUT)
